export function buildUserPrompt(input: UserInput, tone: ToneKey): string {
  const sceneLabel = getScene(input.scene).label;
  const target = input.target?.trim()
    ? input.target.trim()
    : "（未填写，请根据状态合理推断）";
  const avoid = input.avoid?.trim()
    ? input.avoid.trim()
    : "（未特别说明）";

  return `使用场景：${sceneLabel}
我现在的状态：「${input.state}」
我希望听完后变成的状态：「${target}」
我不想听到的内容：「${avoid}」

请按 ${TONE_GUIDE[tone]}
先内部完成"表层事件 + 真正目标 + 反向念头 + 核心缺口 + 新身份认定"的心理拆解，再生成 1 到 8 句肯定语和一句核心心声，并按系统要求只返回 JSON。

场景贴合要求：
- 如果用户说面试、考试、offer、岗位，肯定语要回应"岗位/机会/表达/结果/被看见"。
- 如果用户说复合、联系、他/她、关系、后续，肯定语要回应"主动靠近/被选择/连接恢复/关系推进"。
- 如果用户说客户、事业、收入、买单、合作，肯定语要回应"客户/价值/付费/合作/收入"。
- 如果用户说睡前、内耗、反复想，肯定语要回应"休息/平静/身体/夜晚/安定"。
- 不允许生成和用户场景无关的通用套话。

示例输出格式（仅示例，请根据我的内容重写；注意 affirmations/anchorLine 全为现在时正面、无任何否定词）：
{"title":"《我现在稳稳地表达自己》","scene":"面试前","emotionTags":["紧张","压力","自我怀疑"],"understanding":"你不是能力不足，而是对未知结果感到失控。","targetState":"稳定、自信、可行动","strategy":["降低比较感","恢复掌控感","强化行动感"],"affirmations":["我现在沉稳而清晰，我从容地表达自己。","我已经准备充分，我值得这个机会。","获得理想结果的我，正在稳定展开新的生活。"],"anchorLine":"我此刻沉稳、清晰，我自信地表达自己。","suggestedSoundscape":"confidence","mood":"firm"}`;
}
